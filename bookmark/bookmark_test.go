package bookmark

import (
	"context"
	"os"
	"testing"
)

func TestBookmarkService_Import(t *testing.T) {
	file, err := os.Open("path to import bookmark html file")
	if err != nil {
		t.Fatal(err)
	}
	defer file.Close()

	s := NewBookmarkService()
	folders, err := s.Import(context.Background(), file)
	if err != nil {
		t.Fatal(err)
	}
	t.Log(len(folders))
}
